// --- Controle de versÃ£o do cache ---
const CACHE_NAME = 'jachegou-v2';
const STATIC_ASSETS = [
    '/manifest.webmanifest',
    '/icons/icon-192.png',
    '/icons/icon-512.png'
];

// --- Instala e prÃ©-carrega Ã­cones bÃ¡sicos ---
self.addEventListener('install', e => {
    self.skipWaiting(); // forÃ§a ativaÃ§Ã£o imediata
    e.waitUntil(
        caches.open(CACHE_NAME).then(cache => cache.addAll(STATIC_ASSETS))
    );
});

// --- Ativa nova versÃ£o e remove caches antigos ---
self.addEventListener('activate', e => {
    e.waitUntil(
        caches.keys().then(keys =>
            Promise.all(keys.map(key => {
                if (key !== CACHE_NAME) {
                    console.log('ðŸ§¹ Limpando cache antigo:', key);
                    return caches.delete(key);
                }
            }))
        ).then(() => clients.claim())
    );
});

// --- EstratÃ©gia de busca ---
// Nunca cacheia HTML (para sempre pegar a versÃ£o atual)
self.addEventListener('fetch', e => {
    const req = e.request;

    // evita cache para pÃ¡ginas HTML
    if (req.headers.get('accept')?.includes('text/html')) {
        e.respondWith(fetch(req));
        return;
    }

    // tenta cache para Ã­cones e manifest
    e.respondWith(
        caches.match(req).then(res => res || fetch(req))
    );
});

// --- Web Push Notifications ---
self.addEventListener('push', event => {
    const data = event.data ? event.data.json() : { title: 'JÃ¡ Chegou', body: 'AtualizaÃ§Ã£o disponÃ­vel.' };
    event.waitUntil(
        self.registration.showNotification(data.title, {
            body: data.body,
            icon: '/icons/icon-192.png',
            badge: '/icons/icon-192.png',
            data: data.url ? { url: data.url } : undefined
        })
    );
});

// --- Ao clicar na notificaÃ§Ã£o ---
self.addEventListener('notificationclick', event => {
    event.notification.close();
    const url = (event.notification.data && event.notification.data.url) || '/';
    event.waitUntil(
        clients.matchAll({ type: 'window' }).then(list => {
            for (const client of list) {
                if (client.url === url && 'focus' in client) return client.focus();
            }
            if (clients.openWindow) return clients.openWindow(url);
        })
    );
});
